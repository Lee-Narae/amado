package com.spring.app.domain;

public class ClubmemberVO {

	private String fk_userid, sportseq, clubseq, status;

	public String getFk_userid() {
		return fk_userid;
	}

	public void setFk_userid(String fk_userid) {
		this.fk_userid = fk_userid;
	}

	public String getSportseq() {
		return sportseq;
	}

	public void setSportseq(String sportseq) {
		this.sportseq = sportseq;
	}

	public String getClubseq() {
		return clubseq;
	}

	public void setClubseq(String clubseq) {
		this.clubseq = clubseq;
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}
	
	
	
}
